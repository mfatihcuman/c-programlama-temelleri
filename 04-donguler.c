 #include <stdio.h>
  int main()
  {
    int i = 6;
    printf("%d\n",i);
    
    i += 5;  //i = i + 5
    printf("%d\n",i);
    
    i -= 5;  //i = i - 5
    printf("%d\n",i);
    
    i *= 7;  //i = i * 7
    printf("%d\n",i);
    
    i /= 5;  //i = i / 5
    printf("%d\n",i);
    
    return 0;
  }
